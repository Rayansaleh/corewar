t_op		*gopt(void)
{
	return (g_op_tab);
}

t_op		get_op_name(char *cmd)
{
	int i;

	i = 0;
	while (i < OP_TAB_LENGTH)
	{
		if (ft_strequ(gopt()[i].name, cmd))
			return (gopt()[i]);
		i++;
	}
	return (gopt()[OP_TAB_LENGTH]);
}

t_op		get_op_cmd(int opcode)
{
	if (opcode > 16 || opcode < 1)
	{
		return (gopt()[OP_TAB_LENGTH]);
	}
	return (gopt()[opcode - 1]);
}
